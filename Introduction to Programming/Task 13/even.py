
# We make use of the while loop repetition structure so the program prints out
# all the even numbers from 1 up to (and possibly including) the number
# given by the user

# We declare the variable below:

num = 0

i = 2

# We ask the user to enter a number. 

user_num = int(input("\nPlease enter a number : "))

# We use the while loop condition to check and display all the even numbers between and possibly
# including the entered number

while num < user_num:
    num += i

    print(num)

   

   
      
    


